RESOLUTION_STATUSES = ["Устранено", "В работе", "Просрочено"]
RES_WEIGHTS = [0.60, 0.30, 0.10]


def random_date(start: datetime, end: datetime) -> datetime:
    delta = end - start
    return start + timedelta(seconds=random.randint(0, int(delta.total_seconds())))


def add_seasonality(base_date: datetime) -> float:
    """Летом и осенью (полевой сезон) инцидентов больше."""
    month = base_date.month
    if month in [6, 7, 8, 9]:
        return 1.5
    elif month in [12, 1, 2]:
        return 0.7
    return 1.0


def generate_incidents(n: int = 600) -> pd.DataFrame:
    start = datetime(2023, 1, 1)
    end = datetime(2025, 12, 31)

    # Более рискованные организации — больше инцидентов
    org_risk = {
        "org_01": 0.15, "org_02": 0.22, "org_03": 0.18,
        "org_04": 0.12, "org_05": 0.16, "org_06": 0.10, "org_07": 0.07,
    }
    org_ids = list(org_risk.keys())
    org_weights = list(org_risk.values())

    rows = []
    for _ in range(n):
        date = random_date(start, end)
        season_mult = add_seasonality(date)

        # Немного сдвинуть дату к опасному сезону
        if random.random() < 0.3 * season_mult:
            date = random_date(
                datetime(date.year, 6, 1),
                datetime(date.year, 9, 30),
            )

        inc_type = random.choices(INCIDENT_TYPES, weights=INCIDENT_WEIGHTS)[0]
        org_id = random.choices(org_ids, weights=org_weights)[0]
        org_name = next(o["name"] for o in ORGANIZATIONS if o["id"] == org_id)
        location = random.choice(LOCATIONS)
        cause = random.choice(CAUSES_BY_TYPE[inc_type])
        description = random.choice(DESCRIPTIONS_BY_TYPE[inc_type])
        status = random.choices(INCIDENT_STATUSES, weights=STATUS_WEIGHTS)[0]

        rows.append({
            "id": str(uuid.uuid4()),
            "date": date.strftime("%Y-%m-%d"),
            "time": date.strftime("%H:%M"),
            "type": inc_type,
            "org_id": org_id,
            "org_name": org_name,
            "location": location,
            "description": description,
            "cause": cause,
            "status": status,
        })

    df = pd.DataFrame(rows).sort_values("date").reset_index(drop=True)
    return df


def generate_korgau(n: int = 1200) -> pd.DataFrame:
    start = datetime(2023, 1, 1)
    end = datetime(2025, 12, 31)

    org_ids = [o["id"] for o in ORGANIZATIONS]
    org_names = {o["id"]: o["name"] for o in ORGANIZATIONS}

    rows = []
    for _ in range(n):
        date = random_date(start, end)
        obs_type = random.choices(OBSERVATION_TYPES, weights=OBS_WEIGHTS)[0]
        org_id = random.choice(org_ids)
        org_name = org_names[org_id]

        if obs_type == "Хорошая практика":
            category = random.choice(VIOLATION_CATEGORIES)
            description = random.choice(GOOD_DESCRIPTIONS)
            resolved = "Устранено"
        else:
            category = random.choices(
                VIOLATION_CATEGORIES,
                weights=[0.25, 0.15, 0.12, 0.12, 0.10, 0.08, 0.10, 0.08],
            )[0]
            description = random.choice(VIOLATION_DESCRIPTIONS[category])
            resolved = random.choices(RESOLUTION_STATUSES, weights=RES_WEIGHTS)[0]

        rows.append({
            "id": str(uuid.uuid4()),
            "date": date.strftime("%Y-%m-%d"),
            "obs_type": obs_type,
            "org_id": org_id,
            "org_name": org_name,
            "category": category,
            "description": description,
            "resolved": resolved,
        })

    df = pd.DataFrame(rows).sort_values("date").reset_index(drop=True)
    return df


